  var seed = Date.now();
  
  // 随机选择身份
  var identity = productData.identities[seed % productData.identities.length];
  
  // 随机选择痛点
  var painpointObj = productData.painpoints[seed % productData.painpoints.length];
  
  // 随机选择3个功能点
  var selectedFeatures = [];
