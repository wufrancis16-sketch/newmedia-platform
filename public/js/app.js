function renderCommentPosts(grid, data) {
  var posts = data.posts || [];
  var filtered = posts;
  if (commentFilterKeyword !== 'all') {
    filtered = posts.filter(function(p) { return p.keyword === commentFilterKeyword; });
  }

  if (filtered.length === 0) {
    grid.innerHTML = '<div class="comment-empty"><div class="comment-empty-icon">📭</div><div class="comment-empty-title">该关键词暂无分析数据</div></div>';
    return;
  }

  grid.innerHTML = filtered.map(function(post, idx) {
    return renderPostCard(post, idx, data);
  }).join('');
}

