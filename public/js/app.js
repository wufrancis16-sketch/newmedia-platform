// 在帖子卡片内渲染AI生成文案
function renderGenPostInline(keyword, data) {
  var genData = data && data.generated_content;
  if (!genData || !genData.posts || genData.posts.length === 0) return '';

  var genPosts = genData.posts.filter(function(p) { return p.keyword === keyword; });
  if (genPosts.length === 0) return '';

  var cards = genPosts.map(function(genPost, idx) {
    var tagsHtml = (genPost.tags || []).map(function(t) { 
      return '<span class="gen-inline-tag">' + t + '</span>'; 
    }).join('');

    var bodyLines = (genPost.body || '').split('\n').map(function(line) {
      var trimmed = line.trim();
      if (!trimmed) return '';
      if (trimmed.startsWith('✅')) {
        return '<div class="gen-inline-feature">' + trimmed + '</div>';
      }
      if (trimmed.startsWith('💡')) {
        return '<div class="gen-inline-tip">' + trimmed + '</div>';
      }
      return '<div class="gen-inline-text">' + trimmed + '</div>';
    }).join('');

    var painLabel = genPost.pain_source ? '<span class="gen-inline-pain-badge">💬 ' + genPost.pain_source + '</span>' : '';

    return '<div class="gen-inline-card">' +
      '<div class="gen-inline-header">' +
        '<span class="gen-inline-product">' + (genPost.product || '') + ' · ' + (genPost.price || '') + '</span>' +
        '<span class="gen-inline-scene">' + (genPost.scene_type || '') + '</span>' +
        painLabel +
      '</div>' +
      '<div class="gen-inline-post-title">' + (genPost.title || '') + '</div>' +
      '<div class="gen-inline-body">' + bodyLines + '</div>' +
      '<div class="gen-inline-footer">' +
        '<div class="gen-inline-tags">' + tagsHtml + '</div>' +
        '<button class="gen-inline-copy" onclick="copyGenPostInline(this)" data-post="' + 
          encodeURIComponent(JSON.stringify(genPost)) + '">📋 复制文案</button>' +
      '</div>' +
    '</div>';
  }).join('');

  return '<div class="comment-section gen-inline-section">' +
    '<div class="comment-section-title gen-inline-title">' +
      '🤖 AI生成推广文案 <span style="font-size:11px;color:var(--text-muted);font-weight:400">基于评论区高频痛点自动生成 · ' + genPosts.length + '篇</span>' +
    '</div>' +
    '<div class="gen-inline-grid">' + cards + '</div>' +
  '</div>';
}

function renderCommentHistory(container, historical) {
  if (!container) return;
  if (!historical || historical.length === 0) {
    container.style.display = 'none';
    return;
  }

  container.style.display = 'block';
  var cards = historical.map(function(h) {
    var s = h.summary || {};
    return '<div class="comment-history-card">' +
      '<div class="comment-history-date">' + h.date + '</div>' +
      '<div class="comment-history-posts">' + (s.total_posts || 0) + '</div>' +
      '<div style="font-size:11px;color:var(--text-muted);">分析帖子</div>' +
      '<div class="comment-history-comments">' + (s.total_comments || 0) + ' 条评论</div>' +
      '<div style="font-size:11px;color:var(--text-muted);margin-top:2px;">热门行业: ' + (s.top_industry || '-') + '</div>' +
    '</div>';
  }).join('');

  container.innerHTML = '<div class="comment-history-title">📈 历史趋势 <span style="font-size:12px;color:var(--text-muted);font-weight:400;">近7天</span></div>' +
    '<div class="comment-history-grid">' + cards + '</div>';
}

// 复制卡片内AI生成文案
function copyGenPostInline(btn) {
  try {
    var data = JSON.parse(decodeURIComponent(btn.getAttribute('data-post')));
    var text = '【标题】\n' + data.title + '\n\n【正文】\n' + data.body + '\n\n【话题】\n' + (data.tags || []).join(' ');
    if (navigator.clipboard) {
      navigator.clipboard.writeText(text).then(function() {
        btn.textContent = '✅ 已复制';
        setTimeout(function() { btn.textContent = '📋 复制文案'; }, 2000);
      });
    }
  } catch(e) {}
}

