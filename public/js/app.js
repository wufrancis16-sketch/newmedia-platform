function getDateSeed() {
  const now = new Date();
  return now.getFullYear() * 10000 + (now.getMonth() + 1) * 100 + now.getDate();
}

function seededShuffle(array, seed) {
  const arr = [...array];
  let s = seed;
  for (let i = arr.length - 1; i > 0; i--) {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    const j = s % (i + 1);
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function getDailyTopics(platform) {
  const seed = getDateSeed();
  const topics = TOPIC_DATA[platform] || [];
  return seededShuffle(topics, seed).slice(0, 8);
}

function getDailyTitles(category) {
  const seed = getDateSeed();
  if (category && category !== 'all') {
    const cat = TITLE_DATA.find(c => c.category === category);
    if (cat) return seededShuffle(cat.titles, seed + cat.titles.length);
    return [];
  }
  // 全部分类时，每个分类取前几个
  const result = [];
  TITLE_DATA.forEach(cat => {
    const shuffled = seededShuffle(cat.titles, seed + cat.category.length);
    result.push(...shuffled.slice(0, 3));
  });
  return result;
}

