// ============================================================
// 标题推荐渲染
// ============================================================
function renderTitlesPage(filterCategory) {
  const container = document.getElementById('titleRecommendations');
  const filterTabs = document.getElementById('titleFilterTabs');
  const countEl = document.getElementById('titleFilterCount');
  const dateEl = document.getElementById('titleDate');
  if (!container) return;

  // 更新日期
  if (dateEl) {
    const now = new Date();
    const dateStr = `${now.getFullYear()}年${now.getMonth() + 1}月${now.getDate()}日`;
    const weekdays = ['周日', '周一', '周二', '周三', '周四', '周五', '周六'];
    dateEl.textContent = `📅 ${dateStr} ${weekdays[now.getDay()]} · 基于爆款公式，为你推荐不同角度的吸睛标题`;
  }

  // 初始化筛选标签
  if (filterTabs && !filterTabs.hasChildNodes()) {
    const allCount = TITLE_DATA.reduce((sum, cat) => sum + cat.titles.length, 0);
    const allTab = document.createElement('button');
    allTab.className = 'title-filter-tab active';
    allTab.dataset.category = 'all';
    allTab.textContent = `全部（${allCount}）`;
    allTab.onclick = () => filterTitlesByCategory('all');
    filterTabs.appendChild(allTab);

    TITLE_DATA.forEach(cat => {
      const tab = document.createElement('button');
      tab.className = 'title-filter-tab';
      tab.dataset.category = cat.category;
      tab.textContent = `${cat.icon} ${cat.category}`;
      tab.onclick = () => filterTitlesByCategory(cat.category);
      filterTabs.appendChild(tab);
    });
  }

  // 更新标签激活状态
  if (filterTabs) {
    filterTabs.querySelectorAll('.title-filter-tab').forEach(tab => {
      tab.classList.toggle('active', tab.dataset.category === (filterCategory || 'all'));
    });
  }

  // 筛选数据（每日轮换）
  const filteredData = filterCategory && filterCategory !== 'all' 
    ? TITLE_DATA.filter(cat => cat.category === filterCategory)
    : TITLE_DATA;

  // 计算总数
  const totalCount = filteredData.reduce((sum, cat) => sum + cat.titles.length, 0);
  if (countEl) {
    countEl.textContent = `今日推荐 ${totalCount} 个标题`;
  }

  container.innerHTML = filteredData.map(cat => {
    const dailyTitles = seededShuffle(cat.titles, getDateSeed() + cat.category.length);
    return `
    <div class="title-category">
      <div class="title-category-header">
        <span class="title-category-icon">${cat.icon}</span>
        <span class="title-category-name">${cat.category}</span>
        <span class="title-category-count">${dailyTitles.length} 个标题</span>
      </div>
      <div class="title-list">
        ${dailyTitles.map((t, i) => `
          <div class="title-item">
            <div class="title-item-text">${t.text}</div>
            ${t.analysis ? `<div class="title-analysis">💡 ${t.analysis}</div>` : ''}
            <div class="title-item-meta">
              <div class="title-item-tags">
                ${t.tags.map(tag => `<span class="title-tag">${tag}</span>`).join('')}
              </div>
              <button class="title-item-copy" data-title="${encodeURIComponent(t.text)}" onclick="copyTitle(this.dataset.title)">复制</button>
            </div>
          </div>
        `).join('')}
      </div>
    </div>
  `}).join('');
}

function filterTitlesByCategory(category) {
  renderTitlesPage(category);
}

function copyTitle(encodedText) {
  const text = decodeURIComponent(encodedText);
  navigator.clipboard.writeText(text).then(() => {
    // 显示复制成功提示
    const toast = document.createElement('div');
    toast.style.cssText = 'position:fixed;top:20px;right:20px;background:#10B981;color:#fff;padding:10px 20px;border-radius:8px;font-size:13px;z-index:9999;';
    toast.textContent = '✅ 已复制到剪贴板';
    document.body.appendChild(toast);
    setTimeout(() => toast.remove(), 1500);
  }).catch(err => {
    // 如果clipboard API失败，使用备用方法
    const textarea = document.createElement('textarea');
    textarea.value = text;
    textarea.style.position = 'fixed';
    textarea.style.opacity = '0';
    document.body.appendChild(textarea);
    textarea.select();
    document.execCommand('copy');
    document.body.removeChild(textarea);
    
    const toast = document.createElement('div');
    toast.style.cssText = 'position:fixed;top:20px;right:20px;background:#10B981;color:#fff;padding:10px 20px;border-radius:8px;font-size:13px;z-index:9999;';
    toast.textContent = '✅ 已复制到剪贴板';
    document.body.appendChild(toast);
    setTimeout(() => toast.remove(), 1500);
  });
}

function exportTitlesToExcel() {
  // 准备数据
  let csvContent = '\uFEFF'; // BOM for UTF-8
  csvContent += '分类,标题,适用平台,分析说明\n';
  
  const now = new Date();
  const dateStr = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`;
  
  TITLE_DATA.forEach(cat => {
    cat.titles.forEach(t => {
      const title = t.text.replace(/"/g, '""');
      const tags = t.tags.join('、');
      const analysis = (t.analysis || '').replace(/"/g, '""');
      csvContent += `"${cat.category}","${title}","${tags}","${analysis}"\n`;
    });
  });
  
  // 创建下载链接
  const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = `标题推荐_${dateStr}.csv`;
  link.click();
  
  // 显示导出成功提示
  const toast = document.createElement('div');
  toast.style.cssText = 'position:fixed;top:20px;right:20px;background:#10B981;color:#fff;padding:10px 20px;border-radius:8px;font-size:13px;z-index:9999;';
  toast.textContent = '✅ 导出成功！';
  document.body.appendChild(toast);
  setTimeout(() => toast.remove(), 1500);
  
  URL.revokeObjectURL(url);
}

