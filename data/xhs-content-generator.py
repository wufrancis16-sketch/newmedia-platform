def generate_all(keywords, analysis_data):
    """为所有关键词生成内容，每个关键词2篇"""
    posts_data = {p["keyword"]: p for p in analysis_data.get("posts", [])}

    all_posts = []
    for kw in keywords:
        post_data = posts_data.get(kw, {})
        comment_insights = extract_comment_pain_points(post_data)
        top_pains = comment_insights["top_pains"]

        # 取前2个痛点，每个生成1篇
        pains_to_use = top_pains[:2]
        # 如果只有1个痛点，补充一个默认的
        if len(pains_to_use) < 2:
            pains_to_use = list(PAIN_TO_SCENE.get(kw, {}).keys())[:2]

        for i, pain_label in enumerate(pains_to_use):
            gen = generate_post_from_pain(kw, pain_label, post_data)

            # 附加额外信息
            gen["product"] = get_product_name(kw)
            gen["price"] = get_product_price(kw)
            gen["pain_comment_ref"] = comment_insights["sample_comments"][:1] if comment_insights["sample_comments"] else []
            all_posts.append(gen)

    output = {
        "generated_at": datetime.now().isoformat(),
        "total": len(all_posts),
        "posts": all_posts,
    }
    return output


