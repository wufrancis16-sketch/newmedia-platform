def generate_all(keywords=None):
    """为所有关键词生成内容"""
    if keywords is None:
        keywords = list(CONTENT_TEMPLATES.keys())

    results = []
    for kw in keywords:
        post = generate_post(kw)
        if post:
            results.append(post)

    output = {
        "generated_at": datetime.now().isoformat(),
        "total": len(results),
        "posts": results,
    }
    return output


def main():
    """主函数：读取评论分析数据，生成内容，合并输出"""
    # 确定输出路径
    script_dir = os.path.dirname(os.path.abspath(__file__))
    analysis_file = os.path.join(script_dir, "xhs-comment-analysis.json")

    # 读取已有的分析数据
    analysis = {}
    if os.path.exists(analysis_file):
        with open(analysis_file, "r", encoding="utf-8") as f:
            analysis = json.load(f)

    keywords = analysis.get("keywords", list(CONTENT_TEMPLATES.keys()))

    # 生成内容
    generated = generate_all(keywords)

    # 合并到分析数据中
    analysis["generated_content"] = generated

    # 写回文件
    with open(analysis_file, "w", encoding="utf-8") as f:
        json.dump(analysis, f, ensure_ascii=False, indent=2)

    print(json.dumps(generated, ensure_ascii=False, indent=2))
    return 0


if __name__ == "__main__":
    sys.exit(main())
