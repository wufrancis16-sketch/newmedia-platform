def extract_comment_pain_points(post_data):
    """从帖子评论数据中提取关键痛点，用于指导内容生成角度"""
    pain_points = post_data.get("pain_points", {})
    comments = post_data.get("representative_comments", [])

    # 按出现次数排序痛点
    sorted_pains = sorted(pain_points.items(), key=lambda x: x[1], reverse=True)

    return {
        "top_pains": [p[0] for p in sorted_pains[:3]],
        "sample_comments": comments[:3],
    }


def generate_post_from_pain(keyword, pain_label, post_data):
    """基于评论区的具体痛点生成一篇推广文案"""

    scene_map = PAIN_TO_SCENE.get(keyword, {})
    pain_config = scene_map.get(pain_label)

    # 如果该痛点没有预置场景，用默认场景
    if not pain_config:
        # 取第一个可用场景
        first_key = next(iter(scene_map.keys()))
        pain_config = scene_map[first_key]

    # 生成标题（基于真实的评论区问题提炼）
    title = generate_title_from_pain(keyword, pain_label, post_data)

    # 构建正文
    body_lines = []
    body_lines.append(pain_config["pain_text"])
    body_lines.append("")
    body_lines.append(f"后来发现了一款好用的{PAIN_TO_SCENE[keyword][list(PAIN_TO_SCENE[keyword].keys())[0]]['scene'] if pain_label not in PAIN_TO_SCENE.get(keyword, {}) else pain_config['scene']}软件，这些问题全解决了！")
    body_lines.append("")

    # 选取功能点（3个）
    features = pain_config["features"]
    for f in features[:3]:
        body_lines.append(f"✅ {f}")

    body_lines.append("")

    # 推荐引导
    closings = CLOSINGS.get(keyword, ["同行朋友们真的可以试试，说不定也能帮到你们！"])
    body_lines.append(random.choice(closings))

    body = "\n".join(body_lines)

    # 话题标签
    tags = TAG_POOL.get(keyword, ["#SaaS", "#效率工具"])
    selected_tags = random.sample(tags, min(4, len(tags)))

    return {
        "keyword": keyword,
        "title": title,
        "body": body,
        "tags": selected_tags,
        "scene_type": pain_config["scene"] if pain_config else "通用场景",
        "pain_source": pain_label,
    }


