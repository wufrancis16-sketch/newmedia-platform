    """从标题库中选择标题"""
    tmpl = CONTENT_TEMPLATES.get(keyword)
    if not tmpl:
        return "这个工具真的太好用了！"
    titles = tmpl["titles"]
    idx = (scene_index + PUBLISH_HISTORY.get(keyword, 0)) % len(titles)
    return titles[idx]


